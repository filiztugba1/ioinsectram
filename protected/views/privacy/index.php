
    <h2>Privacy Policy for Insectram</h2>
	<h3>Introduction</h3>
    <p>This privacy policy describes how Insectram Software Ltd. ("Insectram," "we," "us," or "our") collects, uses, and discloses information from users of our websites located at www.insectram.co.uk and www.insectram.io (the "Websites"), our pest control software (the "Software"), and our mobile application (the "App"). We are committed to protecting the privacy of our users, which includes the data of your clients and their clients (collectively, "End Users").</p>
  	<h3>Information Collected</h3>
    <p>We collect three main types of information:</p>
       <ul>
        <li>
            <strong>Account Information:</strong> This includes information you provide when you create an account on the Websites or the App, such as your name, your employees names and email addresses, and company name etc
        </li>
        <li>
            <strong>Usage Data:</strong> This is information collected automatically through the Software and App about your pest control activities. This data may include the types of pests identified, the locations of pest activity, and the treatments applied. We strive to anonymize this data whenever possible.
        </li>
        <li>
            <strong>End User Data:</strong> This is data provided by you about your clients and their clients, which may include names, email addresses, and building information.
        </li>
    </ul>
	
